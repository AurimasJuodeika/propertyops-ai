export function getComplianceStatus(property) {
  const c = property.compliance
  const checks = [c.epc, c.gasSafety, c.eicr, c.smokeAlarm, c.depositProtection, c.rightToRent]
  if (checks.some(x => x.status === 'expired' || x.status === 'not_verified')) return 'critical'
  if (checks.some(x => x.status === 'expiring_soon' || x.status === 'overdue')) return 'warning'
  if (checks.some(x => x.status === 'n/a')) return 'info'
  return 'compliant'
}

// allProps: pass merged array to include wizard-created properties; defaults to mock PROPERTIES
export function getComplianceSummary(allProps) {
  const props = allProps || PROPERTIES
  const expired = props.filter(p => {
    const c = p.compliance
    if (!c) return false
    return [c.epc, c.gasSafety, c.eicr].some(x => x && (x.status === 'expired' || x.status === 'missing')) ||
      (c.smokeAlarm && c.smokeAlarm.status === 'overdue') ||
      (c.rightToRent && c.rightToRent.status === 'not_verified')
  }).length
  const expiringSoon = props.filter(p => {
    const c = p.compliance
    if (!c) return false
    return !([c.epc, c.gasSafety, c.eicr].some(x => x && (x.status === 'expired' || x.status === 'missing'))) &&
      ([c.epc, c.gasSafety, c.eicr, c.rightToRent].some(x => x && x.status === 'expiring_soon'))
  }).length
  const compliant = props.length - expired - expiringSoon
  return { expired, expiringSoon, compliant, total: props.length }
}

export function getTotalArrears() {
  return TENANCIES.reduce((sum, t) => sum + (t.arrears || 0), 0)
}

export function getArrearsTenancies() {
  return TENANCIES.filter(t => t.arrears > 0).map(t => ({
    ...t,
    property: getPropertyById(t.propertyId),
    tenant: getTenantById(t.tenantId),
  }))
}

// Maintenance job notes store (mutable in localStorage by users)
const JOB_NOTES_KEY = 'propertyops_job_notes'
export function getJobNotes(jobId) {
  try { return JSON.parse(localStorage.getItem(JOB_NOTES_KEY) || '{}')[jobId] || [] } catch { return [] }
}
export function addJobNote(jobId, note, author = 'Sarah Mitchell') {
  try {
    const all = JSON.parse(localStorage.getItem(JOB_NOTES_KEY) || '{}')
    if (!all[jobId]) all[jobId] = []
    all[jobId].unshift({ text: note, author, date: new Date().toLocaleDateString('en-GB'), time: new Date().toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit' }) })
    localStorage.setItem(JOB_NOTES_KEY, JSON.stringify(all))
    return all[jobId]
  } catch { return [] }
}
